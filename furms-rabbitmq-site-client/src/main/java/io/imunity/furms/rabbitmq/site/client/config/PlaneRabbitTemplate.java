package io.imunity.furms.rabbitmq.site.client.config;

import org.springframework.amqp.rabbit.connection.ConnectionFactory;
import org.springframework.amqp.rabbit.core.RabbitTemplate;
import org.springframework.amqp.support.converter.MessageConverter;
import org.springframework.stereotype.Component;

@Component
public class PlaneRabbitTemplate extends RabbitTemplate {
	PlaneRabbitTemplate(ConnectionFactory connectionFactory, MessageConverter converter) {
		super(connectionFactory);
		setMessageConverter(converter);
	}
}
